% Pràctica 3. Control Automàtic.
% Sintonia d'un Compensador PID.
% Métode empíric.
%
% Aquest fitxer permet la sintonía d'un compensador PID, mitjançant
% la gràfica de la resposta temporal i barres lliscants que permeten
% la variació fàcil del paràmetres del controlador (K,Ti,Td).
%

disp('Pràctica 3. Control Automàtic.')
disp('Sintonia d''un Compensador PID.')
disp('Métode empíric.')

clear('Kp','Ti','Td')
if ~exist('FigAjust')
	FigAjust=figure(...
		'Name','Sintonía PID', ...
		'NumberTitle','off');
	axes(...
	'Units','normalized',...
	'Position',[0.06 0.15 0.90 0.75]);
end
if exist('FigAjust')
	figure(FigAjust)
end

% Slider d'ajust de K.
uicontrol(...
	'Style','Text',...
	'String','Kp',...
	'Units','Normalized',...
	'Position',[0.02 0.02 0.03 0.05]);
AjustKp=uicontrol(...
	'Style','slider', ...
	'Units','normalized',...
	'Position',[0.06 0.02 0.22 0.05],...
	'Value',0.1,...
	'Callback','rsposts3');

uicontrol(...
	'Style','Text',...
	'String','Ti',...
	'Units','Normalized',...
	'Position',[0.32 0.02 0.03 0.05]);
AjustTi=uicontrol(...
	'Style','slider',...
	'Units','normalized',...
	'Position',[0.36 0.02 0.22 0.05],...
	'Callback','rsposts3',...
	'Value',1);
uicontrol(...
	'Style','Text',...
	'String','Td',...
	'Units','Normalized',...
	'Position',[0.62 0.02 0.03 0.05]);
AjustTd=uicontrol( ...
	'Style','slider', ...
	'Units','normalized',...
	'Position',[0.66 0.02 0.22 0.05],...
	'Callback','rsposts3');

% Inicialització
rsposts3